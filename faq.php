<h2 class="toohigh">Frequently Asked Questions</h2>

<h3 class="blue">What does this site do exactly?</h3>

<ul class="no-bullets">
<li>
1. Randomize.ink can generate a randomized Lorcana deck based on the parameters that you give it. The idea is: you can <s>exclude</s> certain card types that you don't want to see (like actions, songs, all <span class="redlink bold">Ruby cards</a></span>, all Set 2 cards, etc.) and it will build an exportable, ink-balanced deck that you can play with on Pixelborn. You can have a friend use those same settings to create their own randomized deck and you can battle them in the "Custom" section of Pixelborn. No real strategies, just play with what you get and see what happens!<br /><br />
2. It can also generate "packs of cards" to be used in virtual sealed events. It will generate 6 packs (from either "The First Chapter", "Rise of the Floodborn" or 3 packs from each) and it will lay out all of the cards for you to see. From there, you choose the 40 (or more) cards that you want to use, export them right into Pixelborn and you're ready to compete! I hope these tools will serve you well, please enjoy!<br /><br />
3. Lastly, it can generate an entire virtual draft to be used on Pixelborn! Please see <a href="https://cdn.ravensburger.com/lorcana/tournament-rules#page=10" target="_blank">Ravensburger's official tournament rules</a> for more details</li>
</ul>

<h3 class="blue">Why a random deck?</h3>

<ul class="no-bullets">
<li>The meta can be frustrating. Sometimes, you just can’t handle one more <span class="redlink">“Be Prepared”</span>. Now, you and a friend can agree on the rules, generate random decks, play head-to-head in Custom mode on Pixelborn and just have fun! Want to ban action cards or items? No problem! Ban all Ruby cards? YES PLEASE!</li>
</ul>

<h3 class="blue">Are the generated decks balanced?</h3>

<ul class="no-bullets">
<li>Yes, each deck is guaranteed to have the uninkable count be between 1/6th and 1/4th of your deck size. That way, you get some good uninkable cards but not too many so you have nothing to ink. I just want to make sure that you never see a hand with like 20 uninkable cards because that’s not fun.<br /><br />Also, the generated decks now have 2x to 3x of each inkable card and 1x of each uninkable. This is a way to make sure that you don't have 1x of every single card because that's a little too random.</li>
</ul>

<h3 class="blue">Some cards won’t import properly into Pixelborn</h3>

<ul class="no-bullets">
<li>If you find a card that doesn’t import into Pixelborn properly, please contact me and I’ll send it along to Pavel. It has to do with older card translations that are still hiding in the Pixelborn code somewhere.<br /><br />
Here is the current list of known broken cards:<br /><br />
Te Kā - Heartless<br />
Te Kā - The Burning One<br /><br />
</li>
</ul>

<h3 class="blue">What's the "Sealed Event" thing?</h3>

<ul class="no-bullets">
<li>This is meant to emulate real-life sealed events.<br /><br />First, the site will generate 6 virtual packs of cards and it will lay them all out for you to see. From there, you choose the 40 (or more) cards that you want to use, export them right into Pixelborn and you're ready to compete!<br /><br />
The packs have the same rarity odds as tangible packs: 6 Common, 3 Uncommon, 2 Rare/Super Rare/Legendary and 1 foil card that can be anything from Common to Enchanted!</li>
</ul>

<h3 class="blue">How are the pull rates determined?</h3>

<ul class="no-bullets">
<li>Since we have no official numbers from Ravensburger, the site is using anecdotal pull rates of a shop that opened 60 boxes of Lorcana packs.<br /><br />Each generated pack will include the following:<br /><br />
6 Common*<br />
3 Uncommon<br />
2 Rare/Super Rare/Legendary<br />
1 Foil Card (Anything from Common-Enchanted)**<br /><br />
* (Each pack will have 1 Common of each ink color)<br /><br />
** (Odds of pulling an Enchanted are roughly 1/100 packs)<br /><br />
</li>
</ul>

